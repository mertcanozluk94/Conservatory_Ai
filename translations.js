// RTL diller
const RTL_LANGUAGES = ['ar', 'ur'];

// Default dil
let currentLang = localStorage.getItem('conservatory_lang') || 'tr';

function t(key) {
    const lang = TRANSLATIONS[currentLang] || TRANSLATIONS.tr;
    return lang[key] !== undefined ? lang[key] : (TRANSLATIONS.tr[key] || key);
}

function setLanguage(lang) {
    if (!TRANSLATIONS[lang]) return;
    currentLang = lang;
    localStorage.setItem('conservatory_lang', lang);
    
    // RTL ayarı
    if (RTL_LANGUAGES.includes(lang)) {
        document.documentElement.setAttribute('dir', 'rtl');
    } else {
        document.documentElement.setAttribute('dir', 'ltr');
    }
    document.documentElement.setAttribute('lang', lang);
    
    applyTranslations();
}

function applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        el.textContent = t(key);
    });
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
        const key = el.getAttribute('data-i18n-html');
        el.innerHTML = t(key);
    });
}
